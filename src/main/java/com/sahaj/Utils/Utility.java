package com.sahaj.Utils;

/**
 * Utility Class to have Utils methods.
 * 
 * @author Naren
 *
 */
public class Utility
{
	/**
	 * Validate whether the String is Null or Empty
	 * 
	 * @param value
	 *            String value
	 * @return true if value is null or emptys
	 */
	public static boolean isStringNullOrEmpty(String value)
	{
		return value == null || value.isEmpty();
	}
}
